package org.levacher

fun main(){
    val A:Int=5
    println("int to float: "+A.toFloat())
    val B:Double=5.0
    println("double to int: "+B.toInt())
    val C:String="5"
    println("string to int: "+C.toInt())
    val D:Int=5
    println("int to string: "+D.toString())
    val E:Float=5.0f
    println("float to double: "+E.toDouble())
}